# suma y restas (+ y -)
suma = 12 + 5
resta = 12 - 5

# multi y divicion (* y /)
multi = 12 * 5
divicion = 12 / 5  # devuelve un dato float

# potenciacion (exponente) (**)
exponente = 12**5

# divicion baja
divicion_baja = 12//5  # divide el numero y lo redondea hacia abajo

# modulo o resto
resto = 12 % 5

print(suma)
print(resta)
print(multi)
print(divicion)
print(exponente)
print(divicion_baja)
print(resto)

